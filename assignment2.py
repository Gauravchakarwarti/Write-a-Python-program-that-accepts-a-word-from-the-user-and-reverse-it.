# Write a Python program that accepts a word from the user and reverse it
# Sample Test Case



# Input : Edyoda

# output: adoydE

# solution:

word = input("Enter a word:")
reversed_word = word[::-1]
print("reversed_word:",reversed_word)

word = "Edyoda"
reversed_word = word[::-1]
print("reversed_word:", reversed_word)
